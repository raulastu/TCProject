    public String decode(String code, int[] position, int[] length) {
        StringBuilder s = new StringBuilder(code);
        for (int i = 0; i < position.length; i++) {
            StringBuilder t = new StringBuilder(s.substring(position[i], position[i] + length[i]));
            s.insert(position[i]+length[i], t.reverse());
        }
        return s.toString();
    }

