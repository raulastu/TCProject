    public int selectWord(String[] dictionary, String candidate) {
        int res = -1;
        int max = 0;
        for (int i = 0; i < dictionary.length; i++) {
            int n = 0;
            for (int j = 0; j < dictionary[i].length() && j < candidate.length(); j++) {
                if (dictionary[i].charAt(j) == candidate.charAt(j)) {
                    n++;
                }
            }
            if (n > max) {
                max = n;
                res = i;
            }
        }
        return res;
    }

