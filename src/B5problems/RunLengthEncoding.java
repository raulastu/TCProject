    public String decode(String text) {
        StringBuffer sb = new StringBuffer();
        String n = "";
        for (int i = 0; i < text.length(); i++) {
            if (Character.isDigit(text.charAt(i))) {
                n += text.charAt(i);
            } else {
                if (n.length() == 0) {
                    sb.append(text.charAt(i));
                } else {
                    if (n.length() > 2) {
                        return "TOO LONG";
                    } else {
                        for (int j = 0; j < parseInt(n); j++) {
                            sb.append(text.charAt(i));
                        }
                        n = "";
                    }
                }
                if (sb.length() > 50) {
                    return "TOO LONG";
                }
            }
        }
        return sb.toString();
    }

