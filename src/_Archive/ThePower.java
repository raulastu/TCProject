    public int count(long n) {
        ArrayList<Long> s = new ArrayList<Long>();
        for (int i = 2; i <= 10; i++) {
            for (int j = 2; j <= 10; j++) {
                s.add((long) pow(i, j));
            }
        }
        Queue<Pair> q = new LinkedList<Pair>();
        Pair pa = new Pair(1, 0);
        q.add(pa);
        HashSet<Long> memo = new HashSet<Long>();
        while (!q.isEmpty()) {
            Pair l = q.poll();
            long p = l.a;
            if (p == n) {
                System.err.println(q);
                System.err.println(l);
                return l.cost;
            }
            long x = p;
            if (p > 1) {
                x = p - 1;
                if (!memo.contains(x)) {
                    memo.add(x);
                    q.add(new Pair(x, l.cost + 1));
                }
            }
            x = p + 1;
            if (!memo.contains(x)) {
                memo.add(x);
                q.add(new Pair(x, l.cost + 1));
            }
            for (int i = 2; i < 100; i++) {
                x = (long) pow(p, i);
                if(x>(long) pow(10, 18))
                    break;
                if (!memo.contains(x)) {
                    memo.add(x);
                    q.add(new Pair(x, l.cost + 1));
                }
            }
        }
        System.out.println(s.size());
        return -1;
    }

