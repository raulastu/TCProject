    public int totalPoints(String[] levelMap) {
        int res = 0;
        boolean[][] memo = new boolean[levelMap.length][levelMap[0].length()];
        int i = 0, j = 0;
        int[] di = {0, 1, 0, -1};
        int[] dj = {1, 0, -1, 0};
        int xi = 0;
        int cont = 1;
        memo[0][0] = true;
        res += Integer.parseInt(levelMap[0].charAt(0) + "");
        boolean cc = false;
        int c = 0, d = 0;
        while (cont < memo[0].length * memo.length) {
            int X = i + di[xi % 4];
            int Y = j + dj[xi % 4];
            if (X >= 0 && X < memo.length && Y >= 0 && Y < memo[0].length && !memo[X][Y]) {
                memo[i = X][j = Y] = true;
                cont++;
                res += Integer.parseInt(levelMap[X].charAt(Y) + "");
                if (cc) {
                    res -= Integer.parseInt(levelMap[c].charAt(d) + "");
                    cc = false;
                }
            } else {
                c = i;
                d = j;
                cc = true;
                xi++;
            }
        }
        return res;
    }

