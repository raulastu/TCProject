    public int monthlyPayment(int loan, int interest, int term) {
        long lo = 0;
        long hi = 2000000000L;
        double reali = interest / (double) 12000;
        while (lo < hi) {
            long m = lo + (hi - lo) / 2;
            long after = loan;
            for (int i = 0; i < term * 12; i++) {
                after -= m;
                if (after <= 0)break;
                after = (long) ceil(after * reali + after);                
            }
            if (after >= 0L) {
                lo = m + 1;
            } else {
                hi = m;
            }
        }
        return (int) lo;
    }

