    public double interestRate(double price, double monthlyPayment, int loanTerm) {
        double lo = 0.0;
        double hi = 1.0;
        int i = 0;
        while (i++ < 50) {
            double m = lo + (hi - lo) / (double) 2;
            double balance = price;
            for (int j = 0; j < loanTerm; j++) {
                double interes = m / (double) 12 * balance;
                balance = balance - monthlyPayment + interes;
            }
//            System.err.println(m + " " + balance);
            if (balance < 0) {
                lo = m;
            } else {
                hi = m;
            }
        }
        return lo * 100;
    }

