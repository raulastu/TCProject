    public int getMostWork(int[] folders, int workers) {
        int lo = 0;
        int hi = 0;
        for (int x : folders) {
            hi += x;
            lo = max(lo, x);
        }
        while (lo < hi) {
            int mid = (hi + lo) / 2;
            int required = 1;
            int currentLoad = 0;
            for (int i = 0; i < folders.length; i++) {
                if (currentLoad + folders[i] <= mid) {
                    currentLoad += folders[i];
                } else {
                    required++;
                    currentLoad = folders[i];
                }
            }
            if (required <= workers) {
                hi = mid;
            } else {
                lo = mid + 1;
            }
        }
        return lo;
    }

