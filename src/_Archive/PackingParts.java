    public int pack(int[] partSizes, int[] boxSizes) {
        int res = 0;
        int x = 0;
        for (int i = 0; i < partSizes.length && x < boxSizes.length; i++) {
            if (partSizes[i] <= boxSizes[x]) {
                res++;
            } else {
                i--;
            }
            x++;
        }
        return res;
    }

