    public int cellsLeft(int[] parent, int deletedCell) {
        int res = 0;
        HashSet<Integer> toKill = new HashSet<Integer>();
        toKill.add(deletedCell);
        boolean added = true;
        while (added) {
            boolean ok = false;
            for (int i = 0; i < parent.length; i++) {
                if (toKill.contains(parent[i]) && !toKill.contains(i)) {
                    toKill.add(i);
                    ok = true;
                }
            }
            added = ok;
        }
        for (Integer integer : toKill) {
            parent[integer] = -2;
        }
        for (int i = 0; i < parent.length; i++) {
            if (parent[i] >= 0 && !isParent(i, parent)) {
                res++;
            }
        }
        return res;
    }

    boolean isParent(int x, int[] pa) {
        for (int i = 0; i < pa.length; i++) {
            if (x == pa[i]) {
                return true;
            }
        }
        return false;
    }
