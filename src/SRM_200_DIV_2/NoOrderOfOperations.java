    public int evaluate(String expr) {
        String[] vals = expr.split("[*/+-]");
        int res = Integer.parseInt(vals[0]);
        String[] op = expr.split("[0-9]");
//        print("", vals);
//        print("", op);

        for (int i = 1; i < vals.length; i++) {
//            System.out.println(vals[i]);
            if (op[i].equals("+")) {
                res += Integer.parseInt(vals[i]);
            } else if (op[i].equals("-")) {
                res -= Integer.parseInt(vals[i]);
            } else if (op[i].equals("*")) {
                res *= Integer.parseInt(vals[i]);
            } else if (op[i].equals("/")) {
                res /= Integer.parseInt(vals[i]);
            }
        }
        return res;
    }

