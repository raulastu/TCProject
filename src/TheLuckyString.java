    public int count(String s) {
        int res=0;
        char [] cc = s.toCharArray();
        sort(cc);
        do{
        	if(is(cc))
        		res++;
        }while(nextPerm(cc));        		
        return res;
    }
    
    boolean is(char []cc){
    	for(int i=1;i<cc.length;i++){
    		if(cc[i]==cc[i-1])
    			return false;
    	}
    	return true;
    }
    
    private boolean nextPerm(char[] a) {
        if (a.length <= 1) {
            return false;
        }
        int i = a.length - 1;
        while (a[i - 1] >= a[i]) {
            i--;
            if (i == 0) {
                return false;
            }
        }
        int j = a.length;
        while (a[j - 1] <= a[i - 1]) {
            j--;
            if (j == 0) {
                return false;
            }
        }
        char tmp = a[i - 1];
        a[i - 1] = a[j - 1];
        a[j - 1] = tmp;
        i++;
        j = a.length;
        while (i < j) {
            tmp = a[i - 1];
            a[i - 1] = a[j - 1];
            a[j - 1] = tmp;
            i++;
            j--;
        }
        return true;
    }
